#String
#Implicit
name="Naveen"
print(name)
print(type(name))
#Explicit
name=str("Naveen")
print(name)
print(type(name))
#Data type/Annotation
name:str="Naveen"
print(name)
print(type(name))

#Boolean
a=str(True)
print(a)
print(type(a))

#Octal
a=str(0o34565)
print(a)
print(type(a))

#Binery
a=int(0b10101010)
print(a)
print(type(a))

#Hexadecimal
a=int(0xdad)
print(a)
print(type(a))

#Binery
#integer
a=0b1010101
print(a)
print(type(a))
#External
a=int(0b10101010)
print(a)
print(type(a))
#Data type/Annotation
a:int=0b1010101010
print(a)
print(type(a))









